    private void jButtonEntrarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarLoginActionPerformed
        ConexaoBD conexao = new ConexaoBD();
        TelaPrincipal telaPrincipal = new TelaPrincipal();
        TelaCriarlogin telaCriarLogin = new TelaCriarlogin();
        
        try {
            con.executaSql("select *from controleusuarioslogin where nomeusuario='"+jTextFieldUsuarioLogin.getText()+"'");
            con.rs.first();
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(jTextFieldUsuarioLogin.getText().equals("admin.sistema") && jPasswordFieldSenhaLogin.getText().equals("adminsistema")){
            int resposta = 0;
            
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja Criar um Novo usu치rio de login no sistema?!");
            if(resposta == JOptionPane.YES_OPTION){
                conexao.conecta();
                telaCriarLogin.setVisible(true);
                this.dispose();
            }
            else if(resposta == JOptionPane.NO_OPTION){
                conexao.conecta();
                telaPrincipal.setVisible(true);
                this.dispose();
            }
            
        }
        else try {
            if(con.rs.getString("senhausuario").equals(jPasswordFieldSenhaLogin.getText())){
                telaPrincipal.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Usu치rio e/ou Senha Incorretos!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Usu치rio e/ou Senha Incorretos! \n\nErro: " + ex);
        }
    }//GEN-LAST:event_jButtonEntrarLoginActionPerformed

