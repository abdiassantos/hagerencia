        try {
            conex.rs.first();
            do{
                dados.add(new Object[] {conex.rs.getInt("idproduto"), conex.rs.getString("nomeproduto"), 
                    conex.rs.getDouble("valorproduto"), conex.rs.getInt("quantidadeproduto")});
                
            }while(conex.rs.next());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ArrayList\n erro: " + ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableProdutoEstoque.setModel(modelo);
