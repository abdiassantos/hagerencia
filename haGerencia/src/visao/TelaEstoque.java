        jTableProdutoEstoque.getColumnModel().getColumn(1).setResizable(false);
        jTableProdutoEstoque.getColumnModel().getColumn(2).setPreferredWidth(90);
        jTableProdutoEstoque.getColumnModel().getColumn(2).setResizable(false);
        jTableProdutoEstoque.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableProdutoEstoque.getColumnModel().getColumn(3).setResizable(false);
