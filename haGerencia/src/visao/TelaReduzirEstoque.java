    private void jTableProdutosComQuantidadeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosComQuantidadeMouseClicked
        String nomeproduto = "" + jTableProdutosComQuantidade.getValueAt(jTableProdutosComQuantidade.getSelectedRow(), 1);
        
        try {
            jTextFieldPesquisarProduto.setText(nomeproduto);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar os Dados\nErro: " + ex);
        }
        
        jButtonPesquisaProduto.setEnabled(true);
        jButtonSair.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        
        preencherTabelaAlterarQuantidadeEstoque("select *from estoqueproduto order by nomeproduto");
    }//GEN-LAST:event_jTableProdutosComQuantidadeMouseClicked

