    public void preencherTabelaAlterarQuantidadeEstoque(String Sql){
        
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Nome", "Tamanho", "Quantidade"};
        conex.conecta();
        conex.executaSql(Sql);
        
        try {
            conex.rs.first();
            do{
                dados.add(new Object[] {conex.rs.getInt("idproduto"), conex.rs.getString("nomeproduto"), 
                    conex.rs.getString("tamanhoproduto"), conex.rs.getInt("quantidadeproduto")});
                
            }while(conex.rs.next());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ArrayList\n erro: " + ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableProdutosComQuantidade.setModel(modelo);
        jTableProdutosComQuantidade.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableProdutosComQuantidade.getColumnModel().getColumn(0).setResizable(false);
        jTableProdutosComQuantidade.getColumnModel().getColumn(1).setPreferredWidth(174);
        jTableProdutosComQuantidade.getColumnModel().getColumn(1).setResizable(false);
        jTableProdutosComQuantidade.getColumnModel().getColumn(2).setPreferredWidth(90);
        jTableProdutosComQuantidade.getColumnModel().getColumn(2).setResizable(false);
        jTableProdutosComQuantidade.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableProdutosComQuantidade.getColumnModel().getColumn(3).setResizable(false);
        jTableProdutosComQuantidade.getTableHeader().setReorderingAllowed(false);
        jTableProdutosComQuantidade.setAutoResizeMode(jTableProdutosComQuantidade.AUTO_RESIZE_OFF);
        jTableProdutosComQuantidade.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
                
    }
    
